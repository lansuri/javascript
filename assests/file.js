var total_price=0

function cart_count(){
  document.getElementById("cart").innerHTML = count;
  document.getElementById("cart").style.display = count === 0 ? "none" : "inline";
}
 

function Addtocart(productID){
  var productData = all_products.find(a=>a.Id === productID);
  var existingProduct = cart.find(a=>a.Id === productID)

  if(existingProduct){
    existingProduct.quantity++
  }
  else{
    productData.quantity= 1 
    cart.push(productData);
  }
  count++;
 cart_count()
 Displaycart(cart)
 total_price = productData.Price + total_price
 document.getElementById("totalprice").innerHTML=`Total Price : ₹${total_price}`
 
}


function Displaycart(cartData){
  var content=""
  if(cartData.length === 0){
    content+=`
     <tr>
        <td colspan= "5" class="text-danger text-align-center"> Your card id empty..!</td>
     </tr>
    `
  }
  else{
    cartData.map((product)=>{
      content+=`
      <tr>
        <td>
          <img src="${product.Img}" height="120px"alt="">
        </td>
          <td>${product.Name}</td>
          <td>${product.Price}</td>
          <td>${product.quantity}</td>
          <td><button class="btn btn-danger" onclick="Removefun(${product.Id})">Remove</button></td>
      </tr>
      `
    })
  }
  document.getElementById("cart-remove").innerHTML=content;
}
function Removefun(productID){
  var product=all_products.find(a=>a.Id ===  productID)
   if(product.quantity>1){
    product.quantity--;
   }
   else{
    cart=cart.filter(a=>a.Id !== productID)
   }
   count--;
   cart_count()

   total_price=total_price - product.Price
   document.getElementById("totalprice").innerHTML=`Total Price:₹${total_price}` 
   Displaycart(cart)
}
